#!/bin/bash

# -----------------------------------------------------------
# Simple Interest Calculator (Υπολογιστής Απλού Τόκου)
# Script για υπολογισμό τόκου με βάση τη φόρμουλα: I = P * r * t
# -----------------------------------------------------------

echo "--- Υπολογιστής Απλού Τόκου ---"

# 1. Ζήτηση Κεφαλαίου (Principal)
read -p "Εισάγετε το αρχικό Κεφάλαιο (P): " PRINCIPAL

# 2. Ζήτηση Επιτοκίου (Rate)
read -p "Εισάγετε το Ετήσιο Επιτόκιο (r) ως δεκαδικό (π.χ. 0.05 για 5%): " RATE

# 3. Ζήτηση Χρόνου (Time)
read -p "Εισάγετε τον Χρόνο σε Έτη (t): " TIME

# Έλεγχος αν οι μεταβλητές είναι αριθμοί
if ! [[ "$PRINCIPAL" =~ ^[0-9]+(\.[0-9]+)?$ && "$RATE" =~ ^[0-9]+(\.[0-9]+)?$ && "$TIME" =~ ^[0-9]+(\.[0-9]+)?$ ]]; then
    echo "Σφάλμα: Όλες οι εισαγωγές πρέπει να είναι αριθμοί."
    exit 1
fi

# Υπολογισμός του Απλού Τόκου (I)
# Χρησιμοποιούμε 'bc' για υπολογισμούς κινητής υποδιαστολής (δεκαδικοί)
# scale=2 ορίζει την ακρίβεια σε 2 δεκαδικά ψηφία
INTEREST=$(echo "scale=2; $PRINCIPAL * $RATE * $TIME" | bc)

# Υπολογισμός του Συνολικού Ποσού (A = P + I)
AMOUNT=$(echo "scale=2; $PRINCIPAL + $INTEREST" | bc)


echo "-----------------------------------"
echo "Αποτελέσματα Υπολογισμού:"
echo "Κεφάλαιο (P): $PRINCIPAL"
echo "Επιτόκιο (r): $RATE"
echo "Χρόνος (t): $TIME"
echo "---"
echo "✅ Απλός Τόκος (I): $INTEREST"
echo "💰 Συνολικό Ποσό (A): $AMOUNT"
echo "-----------------------------------"
